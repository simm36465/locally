const electron = require('electron')
const { ipcRenderer, remote  } = electron
const $ = require('jquery');
let win = remote.getCurrentWindow()

$('#minmax').click(function(){
   if(win.isMaximized()){
      $('#minmax').css("color", "#e3e62d")
      $('#minmax').html("&#x02295;")
      win.unmaximize();
  }else{
   $('#minmax').css("color", "#66d61f")
   $('#minmax').html("&#x02296;")
      //win.webContents.send("isminimized", "maxi")
      win.maximize();
  }
  });

$('#close').click(function(){
   //  ipcRenderer.send("close");
   win.close()
 });

 $('#sendURL').click(function() {
    var url = $("#clonWebsite").val();
    if (isValidUrl(url)) {
      ipcRenderer.send("URLCLONE", url)

    } else {
      ipcRenderer.send('open-error-dialog')
    }
 });

ipcRenderer.on('links', (event, links)=>{
  $(".ResultSearch").append("<p>"+links+"</p>")
})
ipcRenderer.on('clearField', (event)=>{
  $(".ResultSearch").html("")
  $('#sendURL').val("")
})

